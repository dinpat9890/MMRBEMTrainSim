package com.sydac.mmrbem.radiointercomplugin.components;

import java.awt.Dimension;

import javax.swing.JButton;

/**
 * <p>
 * Custom Widget for Button
 *
 * @author mulukg
 *
 */
public class CWButton extends JButton
{

  /**
   * 
   */
  private static final long serialVersionUID = 1L;

  public CWButton(String buttonName, Dimension size)
  {

    setPreferredSize(size);
    setSize(size);
    setMaximumSize(size);
    setMinimumSize(size);
    setOpaque(true);
    setText(buttonName);
  }

}
