package com.sydac.mmrbem.tcms.common.data;

/**
 * This class hold data for ED Cutout and RPRESS Isolation status. <br>
 * Note: Data can be hold as {@link Integer}
 */
public class VehicleEdCutoutRpressIsolationStatusDO
{

  /** The car index. */
  private int carIndex;

  private int edCutoutActive;

  private int rpressIsolated;

  /**
   * Gets the car index.
   *
   * @return the car index
   */
  public int getCarIndex()
  {
    return carIndex;
  }

  /**
   * Sets the car index.
   *
   * @param carIndex
   *          the new car index
   */
  public void setCarIndex(int carIndex)
  {
    this.carIndex = carIndex;
  }

  public int getEdCutoutActive()
  {
    return edCutoutActive;
  }

  public void setEdCutoutActive(int edCutoutActive)
  {
    this.edCutoutActive = edCutoutActive;
  }

  public int getRpressIsolated()
  {
    return rpressIsolated;
  }

  public void setRpressIsolated(int rpressIsolated)
  {
    this.rpressIsolated = rpressIsolated;
  }
}