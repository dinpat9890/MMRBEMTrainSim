package com.sydac.mmrbem.tcms.common.data;

/**
 * This class hold data for Brake Cylinder pressure status. <br>
 * Note: Data can be hold as {@link Integer}
 */

public class VehicleBrakeCylinderPressureValueDO
{

  /** The car index. */
  private int carIndex;

  private float bogie1BrakeCylinderPressureValue;

  private float bogie2BrakeCylinderPressureValue;

  /**
   * Gets the car index.
   *
   * @return the car index
   */
  public int getCarIndex()
  {
    return carIndex;
  }

  /**
   * Sets the car index.
   *
   * @param carIndex
   *          the new car index
   */
  public void setCarIndex(int carIndex)
  {
    this.carIndex = carIndex;
  }

  public float getBogie1BrakeCylinderPressureValue()
  {
    return bogie1BrakeCylinderPressureValue;
  }

  public void setBogie1BrakeCylinderPressureValue(float brakeCylinderPressureValue)
  {
    this.bogie1BrakeCylinderPressureValue = brakeCylinderPressureValue;
  }

  public float getBogie2BrakeCylinderPressureValue()
  {
    return bogie2BrakeCylinderPressureValue;
  }

  public void setBogie2BrakeCylinderPressureValue(float bogie2BrakeCylinderPressureValue)
  {
    this.bogie2BrakeCylinderPressureValue = bogie2BrakeCylinderPressureValue;
  }
}
