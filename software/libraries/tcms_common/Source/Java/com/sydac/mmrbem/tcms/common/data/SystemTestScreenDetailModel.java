package com.sydac.mmrbem.tcms.common.data;

/**
 * SystemTestScreenDetailModel acts as a buffer to store the data read from the
 * XML files,
 */

public class SystemTestScreenDetailModel
{

  private int systemId;

  private String systemStatus;

  private String systemName;

  public SystemTestScreenDetailModel(int aSystemId, String aSystemStatus, String name)
  {
    super();
    this.systemId = aSystemId;
    this.systemStatus = aSystemStatus;
    this.systemName = name;
  }

  public SystemTestScreenDetailModel()
  {
    super();

  }

  public int getSystemId()
  {
    return systemId;
  }

  public void setSystemId(int id)
  {
    this.systemId = id;
  }

  public String getSystemStatus()
  {
    return systemStatus;
  }

  public void setSystemStatus(String status)
  {
    this.systemStatus = status;
  }

  public String getSystemName()
  {
    return systemName;
  }

  public void setSystemName(String systemName)
  {
    this.systemName = systemName;
  }
}
