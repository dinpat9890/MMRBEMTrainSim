package com.sydac.mmrbem.tcms.common.data;

/**
 * This class hold data for Fire System Screen - internal Fire Status. <br>
 * Note: Data can be hold as {@link Integer}
 */
public class VehicleInternalFireStatusDO
{

  private int internalFireStatus;

  private int internalFireFaultStatus;

  public int getInternalFireStatus()
  {
    return internalFireStatus;
  }

  public void setInternalFireStatus(int internalFireStatus)
  {
    this.internalFireStatus = internalFireStatus;
  }

  public int getInternalFireFaultStatus()
  {
    return internalFireFaultStatus;
  }

  public void setInternalFireFaultStatus(int internalFireFaultStatus)
  {
    this.internalFireFaultStatus = internalFireFaultStatus;
  }
}