package com.sydac.mmrbem.tcms.common.data;

/**
 * Use this class to capture and hold the single integer value update from
 * comms.
 * 
 * @author rautsw
 */
public class VehicleIntegerDataDO
{
  private int carIndex;

  private int value;

  public VehicleIntegerDataDO()
  {

  }

  // Constructor set the class state.
  public VehicleIntegerDataDO(int aCarIndex, int aNewValue)
  {
    carIndex = aCarIndex;
    value = aNewValue;
  }

  // Gets the car index.
  public int getCarIndex()
  {
    return carIndex;
  }

  // Set the car index
  public void setCarIndex(int carIndex)
  {
    this.carIndex = carIndex;
  }

  // Get the value

  public int getValue()
  {
    return value;
  }

  // Set the value.
  public void setValue(int value)
  {
    this.value = value;
  }
}
