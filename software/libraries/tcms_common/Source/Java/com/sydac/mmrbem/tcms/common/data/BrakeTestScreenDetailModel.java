package com.sydac.mmrbem.tcms.common.data;

/**
 * BrakeTestScreenDetailModel acts as a buffer to store the data read from the
 * XML files,
 */

public class BrakeTestScreenDetailModel
{

  private String carId;
  
  

  private int brakeId;

  private String systemStatus;

  private String compName;

  public BrakeTestScreenDetailModel()
  {
    super();
   
  }
 public int getBrakeId()
  {
    return brakeId;
  }

  public void setBrakeId(int id)
  {
    this.brakeId = id;
  }

  public String getSystemStatus()
  {
    return systemStatus;
  }

  public void setSystemStatus(String status)
  {
    this.systemStatus = status;
  }

  public String getCompName()
  {
    return compName;
  }

  public void setCompName(String systemName)
  {
    this.compName = systemName;
  }
  
  public String getCarId()
  {
    return carId;
  }
 public void setCarId(String carId)
  {
    this.carId = carId;
  }
}
