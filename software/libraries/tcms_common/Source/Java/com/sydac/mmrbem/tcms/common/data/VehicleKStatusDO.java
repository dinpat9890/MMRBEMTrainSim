package com.sydac.mmrbem.tcms.common.data;

/**
 * This class hold data for K Status. <br>
 * Note: Data can be hold as {@link Integer}
 */
public class VehicleKStatusDO
{

  /** The car index. */
  private int carIndex;

  private int kOpen1;

  private int kOpen2;

  /**
   * Gets the car index.
   *
   * @return the car index
   */
  public int getCarIndex()
  {
    return carIndex;
  }

  /**
   * Sets the car index.
   *
   * @param carIndex
   *          the new car index
   */
  public void setCarIndex(int carIndex)
  {
    this.carIndex = carIndex;
  }

  public int getkOpen1()
  {
    return kOpen1;
  }

  public void setkOpen1(int kOpen1)
  {
    this.kOpen1 = kOpen1;
  }

  public int getkOpen2()
  {
    return kOpen2;
  }

  public void setkOpen2(int kOpen2)
  {
    this.kOpen2 = kOpen2;
  }

}