package com.sydac.mmrbem.tcms.common.data;

/**
 * This class hold data for Output Current Value. <br>
 * Note: Data can be hold as {@link Integer}
 */

public class VehicleOutputCurrentValueDO
{

  /** The car index. */
  private int carIndex;

  private int outputCurrent1;

  private int outputCurrent2;

  /**
   * Gets the car index.
   *
   * @return the car index
   */
  public int getCarIndex()
  {
    return carIndex;
  }

  /**
   * Sets the car index.
   *
   * @param carIndex
   *          the new car index
   */
  public void setCarIndex(int carIndex)
  {
    this.carIndex = carIndex;
  }

  public int getOutputCurrent1()
  {
    return outputCurrent1;
  }

  public void setOutputCurrent1(int outputCurrent1)
  {
    this.outputCurrent1 = outputCurrent1;
  }

  public int getOutputCurrent2()
  {
    return outputCurrent2;
  }

  public void setOutputCurrent2(int outputCurrent2)
  {
    this.outputCurrent2 = outputCurrent2;
  }
}
