package com.sydac.mmrbem.tcms.common.data;

/**
 * This class hold data for ED Brake requested status. <br>
 * Note: Data can be hold as {@link Integer}
 */

public class VehicleEdBrakeRequestedValueDO
{

  /** The car index. */
  private int carIndex;

  private int edBrakeRequested;

  /**
   * Gets the car index.
   *
   * @return the car index
   */
  public int getCarIndex()
  {
    return carIndex;
  }

  /**
   * Sets the car index.
   *
   * @param carIndex
   *          the new car index
   */
  public void setCarIndex(int carIndex)
  {
    this.carIndex = carIndex;
  }

  public int getEdBrakeRequested()
  {
    return edBrakeRequested;
  }

  public void setEdBrakeRequested(int edBrakeRequested)
  {
    this.edBrakeRequested = edBrakeRequested;
  }
}
