package com.sydac.mmrbem.tcms.common.data;

/**
 * This class hold data for Output Voltage Value. <br>
 * Note: Data can be hold as {@link Integer}
 */

public class VehicleApsOutputVoltageValueDO
{

  /** The car index. */
  private int carIndex;

  private int outputVoltage1;

  private int outputVoltage2;

  /**
   * Gets the car index.
   *
   * @return the car index
   */
  public int getCarIndex()
  {
    return carIndex;
  }

  /**
   * Sets the car index.
   *
   * @param carIndex
   *          the new car index
   */
  public void setCarIndex(int carIndex)
  {
    this.carIndex = carIndex;
  }

  public int getOutputVoltage1()
  {
    return outputVoltage1;
  }

  public void setOutputVoltage1(int outputVoltage1)
  {
    this.outputVoltage1 = outputVoltage1;
  }

  public int getOutputVoltage2()
  {
    return outputVoltage2;
  }

  public void setOutputVoltage2(int outputVoltage2)
  {
    this.outputVoltage2 = outputVoltage2;
  }
}
