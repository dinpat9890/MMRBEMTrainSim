package com.sydac.mmrbem.dmi.common.state;

public enum DMIAudioModeEnum
{
  AUDIO_20(0, "20%"), AUDIO_40(1, "40%"), AUDIO_60(2, //$NON-NLS-1$ //$NON-NLS-2$
      "60%"), AUDIO_80(3, "80%"), AUDIO_100(4, "100%"); //$NON-NLS-1$ //$NON-NLS-2$ //$NON-NLS-3$

  private final int id;

  private final String audioMode;

  DMIAudioModeEnum(int id, String audioMode)
  {
    this.id = id;
    this.audioMode = audioMode;
  }

  public int getId()
  {
    return id;
  }

  public String getAudioMode()
  {
    return audioMode;
  }
}
