package com.sydac.mmrbem.dmi.common.io.properties;

import static com.sydac.foundation.PropertyKey.create;

import com.sydac.common.febe.binding.CommsBinding;
import com.sydac.foundation.PropertyKey;
import com.sydac.project.runtimedata.ProjectObjectConstants.DMIOutputs;

/**
 * This interface defines {@link PropertyKey} instances for Main Screen
 * Application Outputs.
 *
 */
public interface IAppOutputsMainScreenProperties
{
  @CommsBinding(property = DMIOutputs.CURRENT_DRIVING_MODE)
  PropertyKey<Integer> CURRENT_DRIVING_MODE = create();

  @CommsBinding(property = DMIOutputs.SAFETY_BRAKE_STATUS)
  PropertyKey<Integer> SAFETY_BRAKE_STATUS = create();

  @CommsBinding(property = DMIOutputs.LIFE_INDICATOR_STATUS)
  PropertyKey<Integer> LIFE_INDICATOR_STATUS = create();

  @CommsBinding(property = DMIOutputs.ADVISORY_SPEED)
  PropertyKey<Double> ADVISORY_SPEED = create();

  @CommsBinding(property = DMIOutputs.SERVICE_NUMBER_VALUE)
  PropertyKey<String> SERVICE_NUMBER_VALUE = create();

  @CommsBinding(property = DMIOutputs.FINAL_DESTINATION_NAME)
  PropertyKey<byte[]> FINAL_DESTINATION_NAME = create();

  @CommsBinding(property = DMIOutputs.CURRENT_DATE_AND_TIME)
  PropertyKey<Long> CURRENT_DATE_AND_TIME = create();

  @CommsBinding(property = DMIOutputs.ATC_BYPASS_STATE)
  PropertyKey<Boolean> ATC_BYPASS_STATE = create();

  @CommsBinding(property = DMIOutputs.WARNING_SPEED)
  PropertyKey<Double> WARNING_SPEED = create();

  @CommsBinding(property = DMIOutputs.DESTINATION_CODE)
  PropertyKey<Integer> DESTINATION_CODE = create();

}
